def get_language():
    lang = session.get("lang", request.accept_languages.best_match(["en", "fr"]) if request.accept_languages else "en")
    if lang not in LANGUAGES:
        lang = "en"
    return lang


def _(key):
    lang = get_language()
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, key)


def set_language(lang):
    if lang in LANGUAGES:
        session["lang"] = lang
